    private void txtNopolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtNopolActionPerformed
        // TODO add your handling code here:
        int i=txtNopol.getSelectedIndex();
        if (i==-1)
        {
            return;
        }
         try {
            String kd=(String) txtNopol.getSelectedItem();
            Connection c=database.getkoneksi();
            Statement s= c.createStatement();
            String sql = "Select * from data WHERE nopol=?";
            PreparedStatement p=(PreparedStatement) c.prepareStatement(sql);
            p.setString(1, kd);
            ResultSet result=p.executeQuery();
            result.next();            
            
            this.hargaField.setText(result.getString("harga"));
            this.idLabel.setText(result.getString("id"));
            }catch(SQLException e){
            System.out.println(e);        
         }
    }//GEN-LAST:event_txtNopolActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        try {
             String kd=(String) txtNopol.getSelectedItem();
             String id=(String) idLabel.getText();
             String tarifbaru=this.hargaField.getText();
            Connection c=database.getkoneksi();
            Statement s= c.createStatement();
            String sql = "Update data SET harga=? WHERE id=?";
            PreparedStatement p=(PreparedStatement) c.prepareStatement(sql);
            p.setString(1, tarifbaru);
            p.setString(2, id);
            p.executeUpdate();
            
            p.close();
            data_untuk_combo();
            JOptionPane.showMessageDialog(this, "Sukses Update data");
            backGarasi();
            }catch(SQLException e){
            System.out.println(e);        
         }
    }//GEN-LAST:event_updateBtnActionPerformed

