        private void data_untuk_combo(){
            try {        
        txtNopol.removeAllItems();
        
            Connection c=database.getkoneksi();
            Statement s= c.createStatement();
            String sql="Select nopol from data group by id";
            ResultSet r=s.executeQuery(sql);
            
            while (r.next()) {
                txtNopol.addItem(r.getString("nopol"));
            }
            r.close();
            s.close();
        }catch(SQLException e) {
            System.out.println(e);
        }
}
