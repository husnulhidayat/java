    private void tampilBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tampilBtnActionPerformed
        // TODO add your handling code here:
        database d = new database();
        int i = 0;
        ResultSet rs =null;
        try{
            rs=d.getData("select * from pelanggan");
            while(rs.next()){
                output.setValueAt(rs.getInt("ID"), i, 0);
                output.setValueAt(rs.getString("Nama"), i, 1);
                output.setValueAt(rs.getString("Alamat"), i, 2);
                i++;
            }
        rs.close();        
        }catch(Exception e){
                javax.swing.JOptionPane.showMessageDialog(null,"Error: "+e.getMessage());
        }
    }//GEN-LAST:event_tampilBtnActionPerformed

