    private void hapusBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusBtnActionPerformed
        // TODO add your handling code here:
            database d = new database();
            int row = output.getSelectedRow();
            DefaultTableModel model= (DefaultTableModel)output.getModel();

            String selected = model.getValueAt(row, 0).toString();

            if (row >= 0) {

                model.removeRow(row);

                try {
                    Connection   conn;
                    conn = DriverManager.getConnection("jdbc:mysql://localhost/showroom","root","");
                    PreparedStatement ps = conn.prepareStatement("delete from pelanggan where ID='"+selected+"' ");
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Data Berhasil dihapus!");
                }
                catch (Exception w) {
                    JOptionPane.showMessageDialog(this, "Connection Error!");
                }           
            }
    }//GEN-LAST:event_hapusBtnActionPerformed

