    private void nopolFieldCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_nopolFieldCaretUpdate
        // TODO add your handling code here:
           try {                
            String nopol=(String) this.nopolField.getText();
            Connection c=database.getkoneksi();
            Statement s= c.createStatement();
            String sql="Select * from data Where nopol='"+nopol+"' ";
            ResultSet r=s.executeQuery(sql);
            
            if(r.next()){
                this.labelmerk.setText(r.getString("merk"));
                this.labeljens.setText(r.getString("jenis"));
                this.labelharga.setText(r.getString("harga"));    
               
            }else{
                this.labelmerk.setText("Unknown");
                this.labeljens.setText("Unknown");
                this.labelharga.setText("Unknown");

            }         
            r.close();
            s.close();
        }catch(SQLException e) {
            System.out.println(e);
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(this, "OK");
        }
    }//GEN-LAST:event_nopolFieldCaretUpdate

