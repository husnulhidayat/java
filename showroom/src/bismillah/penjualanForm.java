    private void beliBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beliBtnActionPerformed
        // TODO add your handling code here:
        Penjualan p = new Penjualan();
        p.setNopol(nopolField.getText());
        p.setMerk(labelmerk.getText());
        p.setJenis(labeljens.getText());
        p.setHarga(Integer.parseInt(labelharga.getText()));
        p.setNama(comboPembeli.getSelectedItem().toString());
        p.setCicilan(cicilanField.getText());
        p.setPerbulan(Integer.parseInt(perbulanField.getText()));
        p.savePenjualan();
        nopolField.setText("");
        labelmerk.setText("");
        labelharga.setText("");
        comboPembeli.setToolTipText("");
        cicilanField.setText("");
        perbulanField.setText("");
        
        int confirm = JOptionPane.showConfirmDialog(this,"Apakah anda yakin?");
        if (confirm == JOptionPane.YES_OPTION) {
           JOptionPane.showMessageDialog(this, "Terimakasih "+comboPembeli.getSelectedItem()+", Pembelian Kendaraan dengan\n ------------------------ \n Nomor Polisi : "+nopolField.getText()+"\nMerk : "+labelmerk.getText()+"\nJenis : "+labeljens.getText()+"\nHarga : "+labelharga.getText()+"\nCicilan : "+cicilanField.getText()+"\nPerbulan : "+perbulanField.getText()+"\n ------------------------ \nTelah berhasil!");
           home();
        }
        else{
            JOptionPane.showMessageDialog(this, "Pemebelian dibatalkan");
            home();
        }
    }//GEN-LAST:event_beliBtnActionPerformed

